    public void validar() {
        String dni = txtPass.getText();
        String user = txtUser.getText();
        if (txtPass.getText().equals("") || txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No ha ingresado alguno de los datos");
            txtUser.requestFocus();
        } else {
            entVen = vDAO.Validar(dni, user);
            if (entVen.getUsuario() != null && entVen.getContrasena()!= null) {
                Principal p = new Principal();
                p.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar usuarios validos");
                txtUser.requestFocus();
            }
        }

    }

    
